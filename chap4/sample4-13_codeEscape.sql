-- カラム（項目）を追加する
-- 「構造(Structure)」をクリックして、
-- テーブルの真下に「[]このカラムを追加する（add [] colums(s)）」があることを確認する。
-- 隣の[実行]ボタンをクリックすれば、テーブルを作成する画面に移る。
-- そこで名前とデータ型などを設定して[保存(Save)]をクリックすればカラム追加は完了だ！

-- ここでは、「keyword」という「TEXT」型のカラムを追加したぞ！

-- ちなみに、SQLでカラムを追加する時には「ALTER」という型を使うけど
-- 本書には書いていないので詳しくはググろう。
